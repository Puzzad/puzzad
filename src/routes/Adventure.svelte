<script>
    import {supabase} from "$lib/db.ts";
    import Spinner from '$lib/Spinner.svelte'
    import {push} from "svelte-spa-router";

    export let params = {}

    let details = supabase
        .from('adventures')
        .select(`id,name,description,promoBackground,promoLogo,price`)
        .eq('name', params.name)
        .then(({data, error}) => {
            if (error) {
                throw error
            }
            return data[0]
        })

    let loggedIn = supabase.auth.getSession()
        .then(({data: {session}}) => !!session?.user)

    const addAdventure = async (details) => {
        if (details.price === 0) {
            let {data, error} = await supabase.rpc('addfreeadventure', {adventureid: details.id})
            if (data) {
                await push('/game/' + data)
            }
        }
    }
</script>

{#await details}
    <Spinner />
{:then details}
    <h2><img src="{details.promoLogo}" alt="{details.name}"></h2>
    <section>
    {@html details.description}
    </section>
    {#await loggedIn then isLoggedIn}
        {#if isLoggedIn}
            <button on:click={() => addAdventure(details)}>Start this adventure for {details.price === 0?'free':'Â£'+details.price}</button>
        {:else}
            <p>You must <a href="/#/login">login</a> before you can start an adventure.</p>
        {/if}
    {/await}
{:catch error}
    <p style="color: red">{error.message}</p>
{/await}