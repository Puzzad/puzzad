  <h4>How and why we use your personal information</h4>
  <h5>Your device's internet protocol (IP) address</h5>
  <p>For website visitors, this is a technical necessity in order to deliver the website content to your device. We
    may
    use your IP address if required to combat fraud if required, but will not be used for other purposes.</p>
  <p>For anyone that emails us, this may be supplied to us by your email provider as part of the message data. We do
    not
    explicitly process it, but it is technically infeasible to remove it, so we retain it in the same manner as your
    name and email address, as described below.</p>
  <h5>Your email address or basic third party profile</h5>
  <p>If you register for an account this information will be stored, this is a technical necessity in order to allow
    you
    to securely sign in. We will also request consent to keep in touch for marketing purposes, this is optional and
    not
    required for site operation, you can revoke consent at any time.</p>
  <h5>Your name and email address</h5>
  <p>If you email us or fill out the contact form on our website, we will receive your name and email address. We use
    these in order to reply to you, to action any enquiries you may have, and for any reporting or auditing purposes
    depending on the nature of the message.</p>
  <p>If in the course of sending us a message you supply us with any other personal information, this will be
    processed
    in the same manner as your name and email address, as described above.</p>
  <h5>Your billing details and information about the payment method used</h5>
  <p>If you make a purchase from us, we will receive information about the transaction including your name, billing
    address and details about the payment method (such as the type of card used and the last four digits of the card
    number). We store this information as part of our financial records, to use in case of dispute or fraud, and to
    resolve any support requests that may occur in relation to the purchase.</p>
  <h5>Information entered into the “answer” box</h5>
  <p>During the course of playing our adventures you will need to enter guesses and answers, this is a free form
    textbox
    so any information can be entered here. We store any information entered here to analyse and improve puzzles,
    diagnose issues, and generate statistics. This information is not associated with any personal information we
    hold,
    but we do not have a mechanism for filtering out personal information you may enter into the answer box.</p>
  <h5>Details about your internet browser, internet service provider, and your interactions with the website</h5>
  <p>
    In order to prevent fraud and unwanted automated requests, we employ a 'captcha' system. As part of this
    service, data about your browser, ISP, and details of how you use the website and interact with the 'captcha'
    are recorded and used to determine if the action is being performed by a human or is automated.
  </p>

